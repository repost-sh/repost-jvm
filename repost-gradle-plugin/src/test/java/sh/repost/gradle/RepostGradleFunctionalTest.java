    private static String writeEngine(Path executable) throws IOException, URISyntaxException {
        Path classes = Path.of(FakeRepostSchemaEngine.class.getProtectionDomain()
            .getCodeSource().getLocation().toURI());
        boolean windows = System.getProperty("os.name").startsWith("Windows");
        String java = Path.of(
            System.getProperty("java.home"),
            "bin",
            windows ? "java.exe" : "java"
        ).toString();
        if (windows) {
            executable = executable.resolveSibling(executable.getFileName() + ".cmd");
        }
        Files.writeString(
            executable,
            windows
                ? "@echo off\r\nchcp 65001 >nul\r\nif \"%~1\"==\"capabilities\" goto capabilities\r\n" +
                    "cd /d \"%~dp0\"\r\n" +
                    "for %%F in (\"build\\.repost-sdk-state\\generate\\requests\\.repost-generation-*.json\") do \"" + java +
                    "\" -Dstdout.encoding=UTF-8 -Dstderr.encoding=UTF-8" +
                    " -Xms16m -Xmx128m -XX:+UseSerialGC" +
                    " -XX:ActiveProcessorCount=2 -cp \"" + classes +
                    "\" sh.repost.gradle.FakeRepostSchemaEngine %1 %2 \"%%~F\"\r\n" +
                    "exit /b %errorlevel%\r\n" +
                    ":capabilities\r\n\"" + java +
                    "\" -Dstdout.encoding=UTF-8 -Dstderr.encoding=UTF-8" +
                    " -Xms16m -Xmx128m -XX:+UseSerialGC" +
                    " -XX:ActiveProcessorCount=2 -cp \"" + classes +
                    "\" sh.repost.gradle.FakeRepostSchemaEngine %1 %2\r\n"
                : "#!/bin/sh\n" +
                    "if locale -a 2>/dev/null | grep -Eiq '^C\\.utf-?8$'; then export LC_ALL=C.UTF-8; fi\n" +
                    "exec " + shell(java) +
                    " -Dstdout.encoding=UTF-8 -Dstderr.encoding=UTF-8" +
                    " -Xms16m -Xmx128m -XX:+UseSerialGC -XX:ActiveProcessorCount=2 -cp " +
                    shell(classes.toString()) + " sh.repost.gradle.FakeRepostSchemaEngine \"$@\"\n",
            StandardCharsets.UTF_8
        );
        if (!windows) {
            try {
                Files.setPosixFilePermissions(executable, EnumSet.of(
                    PosixFilePermission.OWNER_READ,
                    PosixFilePermission.OWNER_WRITE,
                    PosixFilePermission.OWNER_EXECUTE
                ));
            } catch (UnsupportedOperationException exception) {
                if (!executable.toFile().setExecutable(true)) {
                    throw new IOException("could not make fake engine executable");
                }
            }
        }
        return executable.getFileName().toString();
    }

    private static GradleRunner runner(Path project) {
        return runner(project, Map.of());
    }

    private static GradleRunner runner(Path project, Map<String, String> additions) {
        return GradleTestVersions.apply(GradleRunner.create()
            .withProjectDir(project.toFile())
            .withPluginClasspath()
            .withEnvironment(testEnvironment(project, additions))
            .withArguments(
                "--offline",
                "--build-cache",
                "--configuration-cache",
                "--parallel",
                "--stacktrace",
                "repostGenerate"
            ));
    }

    private static Map<String, String> testEnvironment(Path project, Map<String, String> additions) {
        Map<String, String> environment = new HashMap<>(System.getenv());
        environment.put("JAVA_HOME", System.getProperty("java.home"));
        environment.put("PATH", "/usr/bin:/bin");
        environment.putAll(additions);
        return environment;
    }

    private static void assertEngineFailure(
        Path project,
        String schema,
        String message,
        String span
    ) throws Exception {
        writeGeneratingProject(project, "");
        Files.writeString(project.resolve("repost/schema.repost"), schema + "\n");
        String output = runner(project).buildAndFail().getOutput();
        assertTrue(output.contains(message));
        if (span != null) {
            Path schemaFile = project.resolve("repost/schema.repost");
            String normalizedOutput = output.replace('\\', '/');
            String expected = schemaFile.toAbsolutePath().normalize().toString().replace('\\', '/');
            String canonical = schemaFile.toRealPath().toString().replace('\\', '/');
            assertTrue(
                normalizedOutput.contains(expected + ":" + span) ||
                    normalizedOutput.contains(canonical + ":" + span),
                output
            );
        }
    }

    private static String generatedText(Path project) throws IOException {
        StringBuilder text = new StringBuilder();
        Path generated = project.resolve("build");
        if (!Files.exists(generated)) {
            return "";
        }
        try (java.util.stream.Stream<Path> paths = Files.walk(generated)) {
            for (Path path : paths.filter(Files::isRegularFile)
                .collect(java.util.stream.Collectors.toList())) {
                text.append(Files.readString(path, StandardCharsets.UTF_8));
            }
        }
        return text.toString();
    }

    private static boolean containsNamed(Path root, String name) throws IOException {
        if (!Files.exists(root)) {
            return false;
        }
        try (java.util.stream.Stream<Path> paths = Files.walk(root)) {
            return paths.anyMatch(path -> path.getFileName().toString().equals(name));
        }
    }

    private static void writeRegistryProject(
        Path project,
        String registryId,
        String generatorId,
        String name
    ) throws IOException {
        Files.createDirectories(project);
        Files.writeString(project.resolve("build.gradle"), "plugins { id 'java' }\n");
        Path registry = project.resolve(
            "src/main/resources/META-INF/repost/registries/v1/" + registryId + "/registry.json"
        );
        Files.createDirectories(registry.getParent());
        Files.writeString(registry, registryJson(registryId, generatorId, name), StandardCharsets.UTF_8);
    }

    private static Path registryJar(
        Path path,
        String registryId,
        String generatorId,
        String name
    ) throws IOException {
        try (JarOutputStream output = new JarOutputStream(Files.newOutputStream(path))) {
            output.putNextEntry(new JarEntry(
                "META-INF/repost/registries/v1/" + registryId + "/registry.json"
            ));
            output.write(registryJson(registryId, generatorId, name).getBytes(StandardCharsets.UTF_8));
            output.closeEntry();
        }
        return path;
    }

    private static String registryJson(String registryId, String generatorId, String name) {
        String type = Character.toUpperCase(name.charAt(0)) + name.substring(1) + "Client";
        return "{\n" +
            "  \"formatVersion\": 1,\n" +
            "  \"registryId\": \"" + registryId + "\",\n" +
            "  \"registryType\": \"module\",\n" +
            "  \"clients\": [\n" +
            "    {\n" +
            "      \"formatVersion\": 1,\n" +
            "      \"generatorId\": \"" + generatorId + "\",\n" +
            "      \"language\": \"java\",\n" +
            "      \"packageName\": \"com.acme." + name + "\",\n" +
            "      \"clientType\": \"com.acme." + name + "." + type + "\",\n" +
            "      \"factoryType\": \"com.acme." + name + "." + type + "Factory\",\n" +
            "      \"schemaHash\": \"sha256:" + "a".repeat(64) + "\",\n" +
            "      \"descriptorVersion\": 2,\n" +
            "      \"runtimeVersion\": \"1.0.0\"\n" +
            "    }\n" +
            "  ]\n" +
            "}\n";
    }

    private static Path temporaryRoot(String name) throws IOException {
        Path base = Path.of(System.getenv().getOrDefault("REPOST_TEST_TMPDIR", System.getProperty("java.io.tmpdir")));
        return Files.createTempDirectory(base, "repost-gradle-" + name + "-").toAbsolutePath();
    }

    private static String shell(String value) {
        return "'" + value.replace("'", "'\\''") + "'";
    }

    private static void deleteTree(Path root) throws IOException {
        if (!Files.exists(root)) {
            return;
        }
        try (java.util.stream.Stream<Path> paths = Files.walk(root)) {
            for (Path path : paths.sorted(java.util.Comparator.reverseOrder())
                .collect(java.util.stream.Collectors.toList())) {
                Files.deleteIfExists(path);
            }
        }
    }
