    @Test
    void preservesEngineOwnedBytesAndAddsOnlyExplicitAggregateGlue() throws Exception {
        Path root = temporaryRoot("entrypoint-parity");
        try {
            Path project = root.resolve("app");
            writeGeneratingProject(project, "");

            BuildResult result = runner(project).build();
            assertEquals(TaskOutcome.SUCCESS, result.task(":repostGenerate").getOutcome());

            Path javaSource = project.resolve(
                "build/generated/sources/repost/javaSdk/java/com/acme/custom/CustomClient.java"
            );
            assertEquals(
                "// Generated by fake Repost engine\n" +
                    "package com.acme.custom;\n" +
                    "public final class CustomClient {}\n",
                Files.readString(javaSource, StandardCharsets.UTF_8)
            );
            Path javaResources = project.resolve("build/generated/resources/repost/javaSdk");
            Path clientManifest;
            try (java.util.stream.Stream<Path> paths = Files.walk(javaResources)) {
                clientManifest = paths
                    .filter(path -> path.getFileName().toString().equals("client.json"))
                    .findFirst()
                    .orElseThrow();
            }
            String clientBytes = Files.readString(clientManifest, StandardCharsets.UTF_8);
            assertTrue(clientBytes.startsWith("{\n  \"formatVersion\": 1,\n"), clientBytes);
            assertTrue(clientBytes.contains("\"clientType\": \"com.acme.custom.CustomClient\""), clientBytes);
            assertTrue(clientBytes.endsWith("\n"), clientBytes);

            Path generated = project.resolve("build/generated");
            try (java.util.stream.Stream<Path> paths = Files.walk(generated)) {
                for (Path file : paths.filter(Files::isRegularFile)
                    .collect(java.util.stream.Collectors.toList())) {
                    String relative = generated.relativize(file).toString().replace('\\', '/');
                    boolean engineOwned = relative.startsWith("sources/repost/") ||
                        relative.startsWith("resources/repost/");
                    boolean ownershipMarker = relative.endsWith("/.repost-output-root.json");
                    boolean aggregateGlue = relative.startsWith("sources/repostRegistry/") ||
                        relative.startsWith("resources/repostRegistry/");
                    assertTrue(
                        engineOwned || ownershipMarker || aggregateGlue,
                        "Gradle added non-aggregate generated output " + relative
                    );
                }
            }
            assertTrue(Files.isDirectory(project.resolve("build/generated/sources/repostRegistry")));
            assertTrue(Files.isDirectory(project.resolve("build/generated/resources/repostRegistry")));
        } finally {
            deleteTree(root);
        }
    }

